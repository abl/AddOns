function unpackByIndex(tab, ...)
    local indexes, res = {...}, {}

    local i, j = 0

    for _, j in ipairs(indexes) do
        i = i + 1
        res[i] = tab[j]
    end

    return unpack(res, 1, i)
end

function splitByRegexp(str, sep, maxParts)
    sep = sep or "%s"

    local res, i = {}, 1

    while str ~= "" do
        if maxParts and i >= maxParts then
            res[i] = str
            return unpack(res)
        end

        local p = { str:match("^(.-)" .. sep .. "(.-)$") }

        if #p < 1 then
            res[i] = str
            return unpack(res)
        end

        res[i], str = p[1], p[#p]
        i = i + 1
    end

    res[i] = str
    return unpack(res)
end

function tableIterator(tab, needUnpack)
    local index

    return function()
        local k, v = next(tab, index)

        if k then
            index = k

            if needUnpack then
                return k, unpack(v)
            end

            return k, v
        end
    end
end

function valuesIterator(tab, needUnpack)
    local index

    return function()
        local k, v = next(tab, index)

        if k then
            index = k

            if needUnpack then
                return unpack(v)
            end

            return v
        end
    end
end

